# ╔═╡ 5fabe2fc-0c5d-4079-969f-aaa653bb8a8d
function plot_piecewise_constant(; n, α)
	V = ngon_vertices(n)

	θ = objective(α)
	i = maximizer_index(θ, V)
	ŷ = maximizer(θ, V)

	α_vals = collect(-π:0.1:π)
	θ_vals = objective.(α_vals)
	i_vals = [maximizer_index(_θ, V) for _θ in θ_vals]
	
	pl = plot(
		size=(300, 300),
		xticks=false, yticks=1:n,
		xlim=(-π, π), ylim=(0.5, n+0.5),
		xlabel="objectif"
	)
	scatter!(
		pl, α_vals, i_vals,
		color=:blue, lw=2, label=nothing
	)
	scatter!(
		pl, [α], [i],
		color=:red, lw=2, markershape=:square, markersize=9,
		label=nothing,
	)

	plot!(pl, legend=:bottomright)
	pl
end

