md"""
On note $t$ les trains et $p$ les chemins temporels qu'ils peuvent emprunter.

Le MAPF est équivalent à un programme linéaire en nombres entiers :
```math
\begin{align*}
\min_{v \in \{0, 1\}^{\mathcal{T} \times \mathcal{P}}} \quad & \sum_{t,p} c_p v_{t,p} & \texttt{somme des couts des chemins} \\
\text{s.c.} \quad & \sum_p v_{t,p} = 1 & \texttt{un chemin par train} \\
& v_{t_1, p_1} + v_{t_2, p_2} \leq 1 \text{ si } p_1 \cap p_2 & \texttt{pas de collision}
\end{align*}
```
"""

