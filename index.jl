Transformer l'algorithme $\mathcal{A}$ pour qu'il construise une distribution sur les sommets $\mathbf{S}$.

Méthode 1 : perturbation
	
```math
\mathcal{A}(\theta) = \mathbb{E}_Z \left[\arg\max_{v \in \mathbf{P}} ~ (\theta + \varepsilon Z)^\top v \right]
```

Méthode 2 : régularisation

```math
\mathcal{A}(\theta) = \arg\max_{v \in \mathbf{P}} ~ \left(\theta^\top v - \Omega(v)\right)
```
""",
