# ╔═╡ e71a88ae-2f9f-43a1-b3f4-f84aaca1603b
ngon_vertices(n) = [[cos(θ), sin(θ)] for θ in 0:2π/n:2π-0.01]

# ╔═╡ fe6b1a09-1506-4482-a02c-03e103548e3a
function plot_halfspace!(pl, h, l; kwargs...)
	if abs(h.a[2]) < 1e-10
		x = h.β / h.a[1]
		plot!(pl, [x, x], [-l , l]; label=nothing, kwargs...)
	else
		y1 = (-l * h.a[1] - h.β) / (-h.a[2])
		y2 = (+l * h.a[1] - h.β) / (-h.a[2])
		plot!(pl, [-l, l], [y1, y2]; label=nothing, kwargs...)
	end
end

# ╔═╡ e1de9e6a-b748-4bce-8195-58dc8c8e2b7a
objective(α) = 0.5 .* [cos(α), sin(α)]

# ╔═╡ e54b33f9-4d23-45d7-a567-1fcd79ad5945
maximizer_index(θ, V) = argmax(dot(θ, v) for v in V)

# ╔═╡ 3bf5d391-0aa5-48c8-b210-c47102718fcc
maximizer(θ, V) = V[maximizer_index(θ, V)]

# ╔═╡ fdfedf95-3a06-44c2-a63c-c24d32f65bd0
function plot_lp(;
	n=5, α=0., ε=0, M=1,
	show_polytope=false, show_hyperplans=false,
	show_sommets=false, number_sommets=false,
	show_objectif=false, show_max=false,
	show_dist=false, show_moyenne=false,
	legend=:bottomright
)
	V = ngon_vertices(n)
	P = polyhedron(convexhull(V...))
	H = hrep(P)
	
	θ = objective(α)
	ŷ = maximizer(θ, V)
	
	pl = plot(
		axis=false, ticks=false, xlim=(-1.2, 1.2), ylim=(-1.2, 1.2),
		size=(300, 300), aspect_ratio=:equal
	)
	
	if show_polytope
		plot!(pl, P; aspect_ratio=:equal, label="polytope", color=:gray, alpha=0.3)
	end
	
	if show_hyperplans
		plot!(Float64[], Float64[], color=:black, lw=2, label="hyperplans")
		for h in H.halfspaces
			plot_halfspace!(pl, h, 1.2; color=:black, lw=2)
		end
	end
	
	if show_sommets
		scatter!(pl, first.(V), last.(V); markersize=7, label="sommets", color=:blue)
		if number_sommets
			annotate!(pl, first.(V) .- 0.15, last.(V), string.(1:5))
		end
	end
	
	if show_objectif
		plot!(
			pl, [0., θ[1]], [0., θ[2]],
			color=:red, arrow=true, lw=2, label="objectif"
		)
	end
	
	if show_max
		scatter!(
			pl, [ŷ[1]], [ŷ[2]];
			color=:red, markersize=9, markershape=:square, label="optimum"
		)
	end

	layer = PerturbedAdditive(_θ -> maximizer(_θ, V); ε=ε, nb_samples=M, seed=63)
	probadist = compute_probability_distribution(layer, θ)
	
	if show_dist
		A = probadist.atoms
		p = probadist.weights
		for i in reverse(eachindex(A))
			for j in 1:i-1
				if A[j] ≈ A[i]
					p[j] += p[i]
					deleteat!(A, i)
					deleteat!(p, i)
				end
				break
			end
		end
		
		scatter!(
			pl, map(first, A), map(last, A);
			markersize=15 .* p .^ 0.5, markercolor=:orange,
			label="distribution"
		)
	end

	if show_moyenne
		ŷΩ = compute_expectation(probadist)
		scatter!(
			pl,
			[ŷΩ[1]],
			[ŷΩ[2]];
			color=:red,
			markersize=9,
			markershape=:square,
			label="moyenne",
		)
	end

	plot!(pl, legend=legend)
	pl
end

# ╔═╡ ef8a7822-cecd-4186-9d94-4bc11ad5bd78
TwoColumn(
	md"""
> Aller le plus loin possible selon une direction, dans un ensemble en forme de dé avec un million de faces

Objectif linéaire + contraintes affines

```math
\begin{align*}
\mathcal{A}(\theta) = \arg\max_v \quad & \theta^\top v \\
\text{s.c.} \quad & Cv \leq d
\end{align*}
```

La solution sera un des sommets $\mathbf{S}$ du polytope $\mathbf{P}$
	
```math
\mathbf{P} =\{v : C v \leq d\} = \mathrm{conv} (\mathbf{S})
```
""",
plot_lp(
	show_polytope=show_polytope1,
	show_hyperplans=show_hyperplans1,
	show_sommets=show_sommets1
)
)

